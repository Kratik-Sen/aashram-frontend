const englishTranslations = Object.fromEntries(Object.keys(textTranslations).map((key) => [key, key]));

export const detectDeviceLanguage = () => {
  const stored = localStorage.getItem("aashram_language");
  if (stored && supportedLanguages[stored]) return stored;

  const languages = navigator.languages?.length ? navigator.languages : [navigator.language];
  return languages.some((language) => String(language).toLowerCase().startsWith("hi")) ? "hi" : "en";
};

export const initI18n = () => {
  if (i18next.isInitialized) return i18next;

  i18next.init({
    lng: detectDeviceLanguage(),
    fallbackLng: "en",
    keySeparator: false,
    interpolation: { escapeValue: false },
    resources: {
      en: { translation: englishTranslations },
      hi: { translation: textTranslations }
    }
  });

  return i18next;
};

export default initI18n();
